import './style.css'
import * as THREE from 'three'
import { OrbitControls } from 'three/examples/jsm/controls/OrbitControls.js'
import * as dat from 'lil-gui'

/**
 * Base
 */
// Debug
const gui = new dat.GUI()

// Canvas
const canvas = document.querySelector('canvas.webgl')

// Scene
const scene = new THREE.Scene()

/**
 * Lights
 */
const ambientLight = new THREE.AmbientLight('white', 0.1)
scene.add(ambientLight)

const hemisphereLight = new THREE.HemisphereLight('blue', 0.1)
scene.add(hemisphereLight)
hemisphereLight.position.y = 1.2
hemisphereLight.position.x = -1.2
const hemisphereLightHelper = new THREE.HemisphereLightHelper(hemisphereLight, 0.1)
scene.add(hemisphereLightHelper)

const pointLight = new THREE.PointLight('red', 0.5)
scene.add(pointLight)
pointLight.position.set(0,0,0)
const pointLightHelper = new THREE.PointLightHelper(pointLight, 0.3)
scene.add(pointLightHelper)

const directionLight = new THREE.DirectionalLight('white', 0.5)
scene.add(directionLight)
directionLight.intensity = 0.1
const directionLightHelper = new THREE.DirectionalLightHelper(directionLight, 0.3)
scene.add(directionLightHelper)

const spotLight = new THREE.SpotLight(0xf0f0f0, 0.7)
scene.add(spotLight)
spotLight.angle = Math.PI * 0.2
spotLight.intensity =0.1
const spotLightHelper = new THREE.SpotLightHelper(spotLight, 0.3)
scene.add(spotLightHelper)


const rectAreaLight = new THREE.RectAreaLight('white', 1)
scene.add(rectAreaLight)




// const pointLight = new THREE.PointLight(0xffffff, 0.5)
